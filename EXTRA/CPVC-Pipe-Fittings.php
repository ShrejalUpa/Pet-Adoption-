      <p>We recommend the use of our CPVC pipes for water supply and distribution in both domestic and industrial applications, and for solar and electric water heaters.</p>
      <p>Ambuja Tube Fitting CPVC pipes are lightweight, non-corrosive and long lasting, which make them a good substitute for GI & conventional pipes. These pipes are manufactured using lead free compounds. Hence they are safe, health and environment friendly.</p>
      <p>Our stringent quality assurance approach across all stages of manufacturing gives this product a high degree of reliability, making Ambuja Tube Fitting CPVC a preferred choice of leading MEP consultants, architects, builders, plumbing contractors,plumbers, and quality-conscious people across the country.</p>
      <p>The CPVC plumbing pipes are available in sizes from 15mm (1/2") to 50mm (2") conforming to IS 15778 in SDR 11 & 13.5 classes. CPVC pipes are also available in ‘Iron Pipe Size’ (IPS) from 65mm (2 1/2") to 150mm (6") in Schedule 40 and Schedule 80 series which meet the requirements of ASTM F 441. These pipes are plain ended and are available in standard lengths of 3 and 5 meters with various pressure ratings.<p>
        <p>Although these pipes have similar physical properties, the classes differ in wall thickness and therefore at any given temperature their pressure ratings will differ. Ambuja Tube Fitting CPVC pipes are recommended for water temperature up to 82°C.</p>
        <p>CPVC FITTINGS</p>
        <p>CPVC fittings are available in sizes from 15 mm (1/2”) to 50 mm (2”) and meet the requirement of ASTM D 2846 in Copper Tube Sizing (CTS). CPVC fittings from 65 mm (2.1/2”) to 150 mm (6”) in Schedule 40 and Schedule 80 are manufactured as per ASTM F 438 & ASTM F 439 respectively.</p>
